# Step 04: Loss Function

critterion = nn.BCELoss()

# Step 05 : optimizer

optimizer = optim.SGD(model.parameters(), lr=0.1)

# lr -> learning rate
# here the optimizer will get change

# Step 06: Training Loop



for epoch in range(10000):
    outputs = model(X)
    loss = criterion(outputs, y)

    optimizer.zero_grad()
    loss.backward()
