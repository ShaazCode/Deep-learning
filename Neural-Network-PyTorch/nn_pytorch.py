# Building an Neural Network using PyTorch

# Step 01: Import

import torch # core library
import torch.nn as nn # nn is neural layer tools as layer & model
import torch.optim as optim #optimizer (weights to update)

# Data Step 02:

X = torch.tensor([[0,0],[0,1],[1,0],[1,1]], dtype=torch.float32)
y = torch.tensor([[0],[1],[1],[0]], dtype=torch.float32)

