class Model(nn.Module):
    def __init__(self):   # constructor it run automaticaly when we create the model
        super(Model, self).__init__()   # __init__ runs first and then define neural networks
        self.fc1 = nn.Linear(2, 2)  # super is a keword that used to connect your class to pytorch base class
        self.fc2 = nn.Linear(2, 1)

    def forward(self, x):
        x = torch.sigmoid(self.fc1(x))
        x = torch.sigmoid(self.fc2(x))
        return x

model = Model()

