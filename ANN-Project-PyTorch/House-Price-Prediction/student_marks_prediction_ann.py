# one hot encoding because here the order is not required

# df.get_dummies is used to convert the categorical data into numeric

df1 = pd.get_dummies(df1, columns=[
    "gender",
    "race/ethnicity",
    "parental level of education",
    "lunch",
    "test preparation course"
])

df1

